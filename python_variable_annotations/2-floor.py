#!/usr/bin/env python3
"""
Has function 'floor'.

Uses Python type hints to specify that
'floor' takes in a float, and returns an int.
"""


def floor(n: float) -> int:
    """
    Assuming that 'n' is a float,
    returns the floored version of 'n',
    which should be an 'int', since 'n'
    is assumed to be a 'float'.
    """
    return int(n)
